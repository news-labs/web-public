export function SeoUrlPreviewPage() {
  const ctx = useTenantContext();
  const [slug, setSlug] = useState("sample-article");
  const [result, setResult] = useState<{ canonical_url: string; hreflang_urls?: Record<string, string> } | null>(null);
  const [loading, setLoading] = useState(false);

  const handlePreview = async () => {
    setLoading(true);
    try {
      const res = await previewUrlPolicy({
        apex_domain: ctx.selectedBrand?.apex_domain ?? "example.com",
        slug,
      });
      setResult(res);
    } finally {
      setLoading(false);
    }
  };

  return (
    <>
      <PageHeader title="URL Policy Preview" description="Canonical and hreflang URL generation" />
      <div className="flex max-w-lg gap-2">
        <Input value={slug} onChange={(e) => setSlug(e.target.value)} placeholder="slug" />
        <Button onClick={handlePreview} disabled={loading}>
          Preview
        </Button>
      </div>
      {result && (
        <div className="mt-4 rounded-xl border p-4 text-sm">
          <p>
            <span className="text-muted-foreground">Canonical:</span> {result.canonical_url}
          </p>
          {result.hreflang_urls && (
            <ul className="mt-2 space-y-1">
              {Object.entries(result.hreflang_urls).map(([loc, url]) => (
                <li key={loc}>
                  {loc}: {url}
                </li>
              ))}
            </ul>
          )}
        </div>
      )}
    </>
  );
}

export function SeoHealthPage() {
  const ctx = useTenantContext();
  const data = useFetch(() =>
    fetchSeoHealth(ctx.brandId ?? undefined, ctx.regionCode ?? undefined),
  );

  if (data.loading) return <LoadingState />;
  if (data.error) return <ErrorState message={data.error} onRetry={data.refetch} />;

  return (
    <>
      <PageHeader title="SEO Health" description="Policy and hreflang checklist" />
      <p className="mb-4 text-2xl font-bold">Score: {data.data?.score ?? 0}</p>
      <ul className="space-y-2">
        {(data.data?.checks ?? []).map((c) => (
          <li key={c.id} className="flex items-center gap-2 rounded-lg border px-4 py-2 text-sm">
            <span className={c.status === "ok" ? "text-green-600" : "text-amber-600"}>{c.status}</span>
            {c.label}
          </li>
        ))}
      </ul>
    </>
  );
}
