import { useState } from "react";
import {
  Button,
  ErrorState,
  Input,
  LoadingState,
  PageHeader,
  useFetch,
  useTenantContext,
} from "@core-labs/admin-shell";
import {
  fetchSeoHealth,
  fetchSeoPolicies,
  previewUrlPolicy,
  upsertSeoPolicy,
} from "../lib/api";

export function SeoMetaPage() {
  const ctx = useTenantContext();
  const scopeType = ctx.regionCode ? "region" : "brand";
  const scopeId = ctx.regionCode ?? ctx.brandId ?? "default";
  const data = useFetch(() => fetchSeoPolicies(scopeType, scopeId));
  const [titleTemplate, setTitleTemplate] = useState("{city_name} News | {brand}");
  const [descTemplate, setDescTemplate] = useState("");
  const [saving, setSaving] = useState(false);

  const existing = data.data?.items?.[0];

  const handleSave = async () => {
    setSaving(true);
    try {
      await upsertSeoPolicy({
        scope_type: scopeType,
        scope_id: scopeId,
        title_template: titleTemplate,
        description_template: descTemplate || undefined,
      });
      data.refetch();
    } finally {
      setSaving(false);
    }
  };

  if (data.loading) return <LoadingState />;
  if (data.error) return <ErrorState message={data.error} onRetry={data.refetch} />;

  return (
    <>
      <PageHeader title="Meta Templates" description="Policy-based SEO — not per-page CRUD" />
      <div className="max-w-xl space-y-4 rounded-xl border p-4">
        <div>
          <label className="text-sm text-muted-foreground">Title template</label>
          <Input
            className="mt-1"
            value={existing?.title_template ?? titleTemplate}
            onChange={(e) => setTitleTemplate(e.target.value)}
            placeholder="{city_name} News | {brand}"
          />
        </div>
        <div>
          <label className="text-sm text-muted-foreground">Description template</label>
          <Input
            className="mt-1"
            value={existing?.description_template ?? descTemplate}
            onChange={(e) => setDescTemplate(e.target.value)}
          />
        </div>
        <Button onClick={handleSave} disabled={saving}>
          {saving ? "Saving…" : "Save policy"}
        </Button>
      </div>
    </>
  );
}

